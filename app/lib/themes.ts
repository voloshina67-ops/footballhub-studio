export type ThemeName = keyof typeof THEME_PRESETS;

export const DEFAULT_THEME: ThemeName = "champions-dark";

export function isThemeName(value: unknown): value is ThemeName {
  return (
    typeof value === "string" &&
    value in THEME_PRESETS
  );
}

export function getThemePreset(value: unknown) {
  return isThemeName(value)
    ? THEME_PRESETS[value]
    : THEME_PRESETS[DEFAULT_THEME];
}
